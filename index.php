          <h4 class="modal-title">Marketing digital:</h4>
          <button type="button" class="close" data-dismiss="modal">&times;</button>
        </div>
        <!-- Modal body -->
        <div class="modal-body oculto-impresion">
          El marketing digital es tan importante como una buena web, es la parte del trabajo invisible que no se ve pero que hay que hacer para salir en los resultados de Google.
          Tener presente que el diseño, la usabilidad, la optimización, las redes y el SEO o SEM + <b>"el tiempo"</b> todo junto harán que tu web se posicione en los primeros resultados.<br /><br />
          A continuación detallo pinceladas del nuevo marketing digital a tener en cuenta para una estrategia a mediano y largo plazo:<br />
          <small>(medio plazo puede ser 4/6 meses, creando contenido, vigilando a la competencia directa, dándole caña a las redes).</small><br />
          <hr>
         <b>+ ¿Como conseguir el éxito en Internet?</b><br />
          No hay un solo factor ni una receta mágica, más bien es una mezcla de varias cosas y estas cosas están en constantes cambios porque la tecnología cambia constantemente. El mercado de masas ya no es el mercado competitivo de hace años, las reglas han cambiado y quien no se adapte a ellas es posible que desaparezca o no crezca.
          Cada día surgen oportunidades y estrategias nuevas, que tenemos que identificar y luego saber aprovecharlas. <b>El éxito en Internet reside en descubrir o crear una necesidad, descubrir si esa necesidad tiene público y descubrir como interactuar con ese público</b>, podríamos interactuar mediante Redes Sociales o simplemente con una web o un simple formulario. Una vez identificado la necesidad y el público hay que ir un paso más allá y crear productos para este público de una forma nueva, ser pioneros.
          <br /><br />
<ul>
<li>La actitud del profesional o vendedor o llamado también <b>servicio posventa</b> tiene que ser diferenciador al resto de la competencia. Muchas veces no es tan importante lo que compramos si no lo amable o atento que ha sido el vendedor. Muchas veces nos da igual a quien se lo vamos a comprar y al final se lleva el pez al agua el vendedor que ha sido más simpático, este detalle marca la diferencia.</li>
<li>Ser capaces de adaptarnos a los infinitos cambios de nuestro entorno, a las tecnologías y a veces a nuestro propio modelo de negocio.</li>
<li>Ser creativos, porque si siempre haces lo mismo siempre vas a tener los mismos resultados, hay que estar informado de las últimas tendencias, dedicar tiempo al I+D, crear cosas nuevas o al menos intentarlo hasta que un día suene la flauta.</li>
  </ul>
 <span class="badge_gabrii_modal">
  El marketing no es solo conseguir visitas y convertirlas, es también la gestión del negocio, cultura de empresa, relaciones con los clientes, proveedores y empleados, todo esto con el tiempo generará mayores ingresos, la fidelización puede y será más rentable que una campaña de publicidad a largo plazo.</span>
          <hr>
        <b>+ Analizar a la competencia</b><br />
Hay que conocer al enemigo antes de que nos ataque, nos jugamos el top ranking entre varias empresas con las mismas keywords o parecidas, <b>conocer a la competencia, estudiarla a fondo, analizar sus Redes y contenidos es pura estrategia</b>. Hay que ver si generan contenido original o de otra gente, si esos post tienen opiniones, comentarios o likes, ver que comparten en Redes y que estrategia siguen, si tienen un diseño impactante, si han conseguido crear una comunidad, si hacen Vblogs y traen a personalidades y expertos, que lenguaje utilizan tanto en las Redes como en los post, hay que escanear la web para saber si es fácil comprar, si la usabilidad se pierde en el carrito, como categorizan los productos y como los llaman, si regalan algo por tu primera compra o por tu primera visita, ¿cómo hacen para que vuelva a visitar su web?, ¿hacen ferias?, ¿y networking?, solo estudiando a nuestras 3/4 empresas de competencia directa, ya tenemos trabajo de sobra, además este estudio de la competencia debe ser trimestral como poco, y por supuesto, seguirlos a todos ellos en su newsletter, YouTube, Facebook, Instagram y lo que haga falta. <i class="fa fa-thumbs-up" style="font-size:18px;color:red"> </i> Un buen truco sería crear una alerta en Google Alerts.
        <hr>
        <b>+ Analizar el mercado</b><br />
        Y es que a lo mejor nuestro producto está saturado en Internet y no tiene la salida que tenía hace unos años, en estos casos te recomendaría ser creativo y darle una vuelta a tu producto y añadir la posventa u otro rasgo diferenciador. A lo mejor no tenemos un claro competidor directo, hemos encontrado un nicho sin competidores directos, pero si los hay y <b>muy fuertes de competidores indirectos</b> y estos se llevan el pastel, un ejemplo de ello seria que hemos inventado una bebida refrescante de un sabor nuevo, no tenemos un competidor directo porque el sabor está patentado por nosotros y no puede haber dos iguales, pero nuestro competidor indirecto es la ultra potente Coca-Cola, no vende lo mismo, <b>pero vende un producto sustitutivo del nuestro.</b>
         <hr>
        <b>+ ¿Como posicionarse en Internet?</b><br />
Todos los anteriores y además:<br /><br />
<ul>
<li>Imágenes optimizadas e impactantes de calidad y propias a ser posible. Una carga rápida ayudará a posicionarte en Google.</li>
<li>Usabilidad y diseño han de ir de la mano, las webs cambian constantemente porque las aplicaciones para móviles pueden reemplazar la web o alguna funcionalidad y no darte ni cuenta.</li>
<li>Contenidos creativos y de calidad para que los visitantes vuelvan y podamos fidelizarlos con el tiempo. <i class="fa fa-thumbs-up" style="font-size:18px;color:red"> </i> Un buen truco sería crear infografías.</li>
<li>Las Redes Sociales para fidelizar con acciones e interactuando con los clientes, escuchándolos y contestando sus inquietudes y dudas, no importa la red, solo tenemos que saber cuál donde esta nuestro público objetivo. Al final las Redes será nuestra <b>Reputación Online</b>.</li>
<li>Un canal de YouTube es interesante porque los vídeos posicionan 50 veces más que un post.</li>
</ul>
        <hr>
        <b>+ Redes Sociales</b><br />
          Las Redes Sociales al final nos darán o nos crearán una reputación online. <b>La reputación no es algo que uno mismo pueda crear, por este motivo tiene el valor que tiene</b>, pero sí que podemos gestionar esa reputación, ¿cómo?, por ejemplo, <i class="fa fa-thumbs-up" style="font-size:18px;color:red"> </i> si un cliente se muestra insatisfecho con un producto nuestro supongamos que le ha llegado dos semanas más tarde de lo que le habíamos informado en la web, y nos deja un comentarío pésimo en alguna red social, podemos escribirle públicamente pidiendo disculpas y además ofreciéndole un descuento en su próxima compra, así el resto de usuarios verán que hay un customer service detrás empatizando con el cliente e intentando solucionar el error.
          La reputación online es como el marketing digital, <b>es una estrategia a medio y largo plazo con mucho trabajo y esfuerzo</b>, pero que da sus frutos con el tiempo, combinándolo con SEO y Redes el objetivo estará cada vez más cerca.
            <br /><br />
          <span class="badge_gabrii_modal">
          En Redes Sociales, el éxito hoy en día es crear una comunidad respecto a tu marca o bien formar parte de una comunidad con público objetivo que puedan ser potenciales clientes para tu marca.</span>
          <br />
<hr>
        <b>+ Neuromarketing - Me emociono, luego pienso</b><br />
          Descartes dijo: Pienso luego existo, lo que René no sabía es que 4 siglos después se demostró que la parte neocórtex del cerebro, antes de pensar ya tiene emociones, y son esas emociones las que nos hacen decidirnos por algo que nos gusta, luego es evidente que pensamos si nos lo podemos permitir o si es el momento. <b>Despertar la curiosidad para que nos conozcan y más tarde nos reconozcan harán que al final nos compren.</b>
